    public Colaborador getColaboradorByNum(int n ){
        List<Colaborador> listaColaborador = f.listaColaboradores;
        Colaborador col = null;
        for(Colaborador c: listaColaborador){
            if(c.getNumeroColaborador() == n){
                col = c;
            }
        }
        return col;
    }

